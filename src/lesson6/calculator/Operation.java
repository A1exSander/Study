package lesson6.calculator;

abstract public class Operation {

    private int a,b;

    public void Operation(int a, int b) {
        this.a = a;
        this.b = b;
    }

    public int getA() {
        return a;
    }

    public void setA(int a) {
        this.a = a;
    }

    public int getB() {
        return b;
    }

    public void setB(int b) {
        this.b = b;
    }

    abstract void execute(int a, int b);
}
